import { readFile, writeFile } from "node:fs/promises";
import { relative } from "node:path";
import ts from "typescript";

const entrypoints = [
  { heading: "Package Root", importPath: "onnx-asr-web", file: "dist/index.d.ts" },
  { heading: "Node Entrypoint", importPath: "onnx-asr-web/node", file: "dist/node.d.ts" },
  { heading: "Browser Entrypoint", importPath: "onnx-asr-web/browser", file: "dist/browser.d.ts" },
];

const program = ts.createProgram({
  rootNames: entrypoints.map((entry) => entry.file),
  options: {
    allowJs: false,
    declaration: true,
    emitDeclarationOnly: true,
    module: ts.ModuleKind.NodeNext,
    moduleResolution: ts.ModuleResolutionKind.NodeNext,
    target: ts.ScriptTarget.ES2020,
  },
});
const checker = program.getTypeChecker();

function getModuleSymbol(sourceFile) {
  return checker.getSymbolAtLocation(sourceFile) ?? sourceFile.symbol;
}

function resolveSymbol(symbol) {
  return symbol.flags & ts.SymbolFlags.Alias ? checker.getAliasedSymbol(symbol) : symbol;
}

function declarationKind(declaration) {
  const kind = ts.SyntaxKind[declaration.kind] ?? "Declaration";
  return kind.replace(/Declaration$/, "").replace(/Signature$/, "");
}

function declarationText(declaration) {
  const text = declaration.getText().trim();
  return text.endsWith(";") || text.endsWith("}") ? text : `${text};`;
}

function docText(symbol) {
  const docs = ts.displayPartsToString(symbol.getDocumentationComment(checker)).trim();
  const tags = symbol.getJsDocTags();
  const tagLines = tags
    .map((tag) => {
      const body = tag.text ? `: ${ts.displayPartsToString(tag.text).trim()}` : "";
      return `- \`@${tag.name}\`${body}`;
    });
  return [docs, ...tagLines].filter(Boolean).join("\n");
}

function stableExports(symbols) {
  return [...symbols].sort((a, b) => a.name.localeCompare(b.name));
}

const sections = [];

for (const entry of entrypoints) {
  const sourceFile = program.getSourceFile(entry.file);
  if (!sourceFile) {
    throw new Error(`Missing declaration file: ${entry.file}`);
  }

  const moduleSymbol = getModuleSymbol(sourceFile);
  if (!moduleSymbol) {
    throw new Error(`Could not resolve module symbol for ${entry.file}`);
  }

  const exports = stableExports(checker.getExportsOfModule(moduleSymbol));
  const exportSections = [];

  for (const exported of exports) {
    const resolved = resolveSymbol(exported);
    const declaration = resolved.declarations?.[0];
    if (!declaration) {
      continue;
    }

    const docs = docText(resolved);
    const kind = declarationKind(declaration);
    const code = declarationText(declaration);
    const definedIn = relative(process.cwd(), declaration.getSourceFile().fileName);

    exportSections.push(
      `### \`${resolved.name}\`\n\n` +
      `Kind: \`${kind}\`  \n` +
      `Defined in: \`${definedIn}\`\n\n` +
      `${docs ? `${docs}\n\n` : ""}` +
      "```ts\n" +
      `${code}\n` +
      "```\n",
    );
  }

  sections.push(
    `## ${entry.heading}\n\n` +
    `Import path: \`${entry.importPath}\`\n\n` +
    exportSections.join("\n"),
  );
}

const output =
  "# API Reference\n\n" +
  "Generated from emitted declaration files in `dist/`. Do not edit this file by hand.\n\n" +
  sections.join("\n");

await readFile("dist/index.d.ts", "utf8");
await writeFile("API.md", output);
